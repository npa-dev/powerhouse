  <h3 style = "color:blue;">Our mission</h3>
 <p>
 We provide advice and support to empower anyone experiencing a mental health problem.<br>
 We campaign to improve services, raise awareness and promote understanding.

 </p>

 <h3 style = "color:blue;">Our vision</h3>
 <p>
 We won't give up until everyone experiencing a mental health problem gets both support<br>
 and respect.<br>
 </p>

<h3 style = "color:blue;">Our goals</h3>
<p>Staying well: Support people likely to develop mental health problems, to stay well.<br>

Empowering choice: Empower people who experience a mental health problem to make informed<br>
choices about how they live and recover<br><br>

Improving services and support: Ensure people get the right services and support at the<br>
right time to help their recovery and enable them to live with their mental health problem<br><br>

Enabling social participation: Open the doors to people with experience of mental health<br>
problems participating fully in society<br><br>

Removing inequality of opportunity: Gain equality of treatment for people who experience<br>
both mental health and other forms of discrimination<br><br>

Organisational excellence: Make the most of our assets by building a culture of excellence.<br>
</p>
<h3 style = "color:blue;">Our Values</h3>
<p>
Powerhouse' values are at the heart of everything we do.<br><br>

<strong>Open:</strong>  We reach out to anyone who needs us.<br><br>

<strong>Together:</strong>  We're stronger in partnerships.<br><br>

<strong> Responsive: </strong>We listen, we act.<br><br>

<strong>Independent:</strong>  We speak out fearlessly.<br><br>

<strong>Loyal:</strong>  We never share your personal stories.<br><br>
</p>
